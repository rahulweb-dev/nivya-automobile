import mongoose from 'mongoose';

const insuranceSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    number: {
      type: Number,
      required: true,
    },
    message: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const Insurance =
  mongoose.models.insurance || mongoose.model('insurance', insuranceSchema);

export default Insurance;
