import mongoose from 'mongoose';

const financeSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    number: {
      type: Number,
      required: true,
    },
    message: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const Finance =
  mongoose.models.finance || mongoose.model('finance', financeSchema);

export default Finance;
